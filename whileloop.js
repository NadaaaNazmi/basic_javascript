// tiga condition
var i = 1
var j = 1
var k = 1
console.log('perulangan for')
for(;i<=5;){
    console.log(i)
    i+=1
}console.log('\n')

//while looping
console.log('perulangan while')
while(j=5){
    console.log(j)
    j+=1
}console.log('\n')

//do while looping
console.log('perulangan do while')
do{
    console.log(k)
    k+=1
}while(k<=5)

//perulangan dengan break
for(l=1;l<=8;l++){
    if(l==2){
        break
    }
    console.log(l)
}console.log('\n')

//perulangan dengan continue
for(m=1;m<=5;m++){
    if(m==2){
        continue
    }
    console.log(m)
}

